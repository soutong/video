package com.sxt.video.model;

public class TeacherInfos {
private int teacherInfoID;
private int userID;
private String teacherName;
private int gender;
private String birthday;
private String email;
private String major;
private String jobTitle;
public int getTeacherInfoID() {
	return teacherInfoID;
}
public void setTeacherInfoID(int teacherInfoID) {
	this.teacherInfoID = teacherInfoID;
}
public int getUserID() {
	return userID;
}
public void setUserID(int userID) {
	this.userID = userID;
}
public String getTeacherName() {
	return teacherName;
}
public void setTeacherName(String teacherName) {
	this.teacherName = teacherName;
}
public int getGender() {
	return gender;
}
public void setGender(int gender) {
	this.gender = gender;
}
public String getBirthday() {
	return birthday;
}
public void setBirthday(String birthday) {
	this.birthday = birthday;
}
public String getEmail() {
	return email;
}
public void setEmail(String email) {
	this.email = email;
}
public String getMajor() {
	return major;
}
public void setMajor(String major) {
	this.major = major;
}
public String getJobTitle() {
	return jobTitle;
}
public void setJobTitle(String jobTitle) {
	this.jobTitle = jobTitle;
}

}
